export type Translations = typeof translations.en;

type I18nState = {
  uiLang: UILanguage;
  setUILang: (lang: UILanguage) => void;
  t: Translations;
};

const I18nContext = createContext<I18nState>({
  uiLang: "en",
  setUILang: () => null,
  t: translations.en,
});

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [uiLang, setUILang] = useState<UILanguage>(
    () => (localStorage.getItem("yozgo-ui-lang") as UILanguage) || "en"
  );

  useEffect(() => {
    localStorage.setItem("yozgo-ui-lang", uiLang);
  }, [uiLang]);

  const value: I18nState = {
    uiLang,
    setUILang: (lang: UILanguage) => setUILang(lang),
    t: translations[uiLang],
  };

  return (
    <I18nContext.Provider value={value}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  return useContext(I18nContext);
}
